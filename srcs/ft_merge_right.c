#include "truc.h"

int			merge_right(int tab[4][4])
{
	int i;
	int	j;
	int vrai;

	vrai = 0;
	j = 3;
	while (j >= 0)
	{
		i = 3;
		while (i >= 0)
		{
			if (tab[i][j] > 0 && tab[i][j] == tab[i - 1][j])
			{
				vrai = 1;
				tab[i][j] *= 2;
				tab[i - 1][j] = 0;
			}
			i--;
		}
		j--;
	}
	return (vrai);
}
