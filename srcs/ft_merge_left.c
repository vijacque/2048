#include "truc.h"

int		merge_left(int tab[4][4])
{
	int i;
	int	j;
	int vrai;

	vrai = 0;
	j = 0;
	while (j < 4)
	{
		i = 0;
		while (i < 4)
		{
			if (tab[i][j] > 1 && tab[i][j] == tab[i + 1][j])
			{
				vrai = 1;
				tab[i][j] *= 2;
				tab[i + 1][j] = 0;
			}
			i++;
		}
		j++;
	}
	return (vrai);
}
