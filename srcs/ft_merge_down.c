int	merge_down(int tab[4][4])
{
	int y;
	int x;
	int vrai;

	vrai = 0;
	y = -1;
	while (++y < 4)
	{
		x = 4;
		while (--x >= 0)
		{
			if (tab[y][x] != 0 && x - 1 >= 0 && tab[y][x - 1] == tab[y][x])
			{
				vrai = 1;
				tab[y][x] *= 2;
				tab[y][x - 1] = 0;
			}
		}
	}
	return (vrai);
}
