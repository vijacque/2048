int		merge_up(int tab[4][4])
{
	int	i;
	int k;
	int vrai;

	vrai = 0;
	i = 0;
	while (i < 4)
	{
		k = 0;
		while (k < 4)
		{
			if (tab[i][k] > 1 && tab[i][k] == tab[i][k + 1])
			{
				vrai = 1;
				tab[i][k] *= 2;
				tab[i][k + 1] = 0;
			}
			k++;
		}
		i++;
	}
	return (vrai);
}
